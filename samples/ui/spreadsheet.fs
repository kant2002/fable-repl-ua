    Комірки : Map<Позиція, string> }

тип Переміщення =
    | ПереміститиВ з Позиція
    | Некоректне

тип Напрямок = Вверх | Вниз | Вліво | Вправо

нехай КнопкиНапрямку : Map<string, Напрямок> = Map.ofList [
  ("ArrowLeft", Вліво)
  ("ArrowUp", Вверх)
  ("ArrowRight", Вправо)
  ("ArrowDown", Вниз)
