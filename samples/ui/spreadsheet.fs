нехай оновлення пов стан =
  співстав пов із
  | ПочатокРедагування(поз) ->
      { стан із ЄАктивним = Some поз }, []

  | ОновитиЗначення(поз, значення) ->
      нехай новіКомірки =
          якщо значення = ""
              тоді Map.remove поз стан.Комірки
              інакше Map.add поз значення стан.Комірки
      { стан із Комірки = новіКомірки }, []

